package java_decision_Statement;

public class Switch_Statement_002 {

	public static void main(String[] args) {
		
		String s = "xy";
		
		switch (s) {
		case "cd":
			System.out.println("Value is cd");
			break;
			
		case "ef":
			System.out.println("Value is ef");
			break;
		case "ab":
			System.out.println("Value is ab");
			break;
			
			default:
				System.out.println("Value is invalid");
				break;
		}
		}

	}


