package java_decision_Statement;

public class Switch_Statement_001 {

	public static void main(String[] args) {
		
		int i =700;
		//Switch Statement

		switch (i) {
		case 100:
			System.out.println("Number is 100");
			break;
			
		case 200:
			System.out.println("Number is 200");
			break;
			
		case 300:
			System.out.println("Number is 300");
			break;
			
		case 400:
			System.out.println("Number is 400");
			break;
			
		case 500:
			System.out.println("Number is 500");
			break;
			
			default:
				System.out.println("Number is Invalid");
				break;
		}
	}

}
