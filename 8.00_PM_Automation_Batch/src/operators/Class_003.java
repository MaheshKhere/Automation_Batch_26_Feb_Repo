package operators;

public class Class_003 {
	
public static void main(String[] args) {
	int i = 2000;
	int r = 20001;
	
	// Equal to 
	System.out.println(i==r);
	
	// Not equal to
	System.out.println(i!=r);
	
	//less than
	System.out.println(i<r);
	
	//less than equal to
	System.out.println(i<=r);
	
	//greater than
	System.out.println(i>r);
	
	//greater than equal to
	System.out.println(i>=r);
}
}
