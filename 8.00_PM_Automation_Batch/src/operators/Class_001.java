package operators;

public class Class_001 {

	public static void main(String[] args) {
		
		int i = 70;
		int a = 30;
		
		// 1st way
		// Addition on two variables
		System.out.println(i + a);
		
		//Subtract 
		System.out.println(i - a);
		
		// Multiplication
		System.out.println(i * a);
		
		// Division- return 
		System.out.println(i / a);

		// Modulus- return remainder 
		System.out.println(i % a);
		
		// Increment - ++
		//System.out.println(++i);
		
		//System.out.println(++a);
		
		//decrement - --
		System.out.println(--i);
		
		System.out.println(--a);
	}

}
