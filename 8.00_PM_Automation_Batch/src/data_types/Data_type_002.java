package data_types;

public class Data_type_002 {

	public static void main(String[] args) {
		
		int i = 3000000;
		
		
		float f = 1000.897f;
		char c = 'a';
		
		// Print int data type
		System.out.println(f);
		System.out.println(c);
		System.out.println(i);
		
		// Output show in same line
		System.out.print(c);
		System.out.print(i);
		
		//Non-Primitive data type-
		String l = "";
		System.out.println(l);
		
		
		String x = "Non-Primitive data type starts with Upper case. ";
		System.out.println();
		System.out.println(l);
		System.out.println(x);
	
	}
}
